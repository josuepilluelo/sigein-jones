    public void cargarDatos(){
        asignacionVehiculosTabla rmvt = new asignacionVehiculosTabla();
        id_vehiculo = rmvt.id_vehiculo;
        System.out.println(id_vehiculo);
        try {
            
            Connection cn1 = Conection_BD.conexion_bd();
            PreparedStatement pst = cn1.prepareStatement("select "
                    + "b.no_inventario, "
                    + "b.marca, "
                    + "b.modelo, "
                    + "b.kilometraje, "
                    + "b.no_motor, "
                    + "b.no_placa, "
                    + "b.cilindraje, "
                    + "b.tarjeta, "
                    + "e.nombre_empleado "
                    + "from vehiculos b JOIN empleados e "
                    + "ON (b.id_empleado = e.id_empleado) "
                    + "WHERE b.id_vehiculo = "+id_vehiculo);
            ResultSet rs = pst.executeQuery();

            //modelo_tabla.
            while (rs.next()) {
                etq_noInventario.setText(rs.getObject(1).toString());
                etq_marca.setText(rs.getObject(2).toString());
                etq_modelo.setText(rs.getObject(3).toString());
                etq_kilometraje.setText(rs.getObject(4).toString());
                etq_motor.setText(rs.getObject(5).toString());
                etq_placa.setText(rs.getObject(6).toString());
                etq_cilindraje.setText(rs.getObject(7).toString());
                etq_tarjeta.setText(rs.getObject(8).toString());
                etq_encargado.setText(rs.getObject(9).toString());

            }

            cn1.close();
        } catch (Exception e) {
            System.out.println("Error al llenar tabla de equipos: " + e);
            //JOptionPane.showMessageDialog(Componente, mensaje, t√≠tulo, tipo de mensaje, icono);
            JOptionPane.showMessageDialog(null, "Error al mostrar informacion de productos", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    
    public void cargarEmpleados(){
        try {
            Connection cn = Conection_BD.conexion_bd();
            PreparedStatement pst = cn.prepareStatement("SELECT nombre_empleado, clave_servidor_publico FROM empleados WHERE nombre_empleado not in ('almacen','transferencia')");
            ResultSet rs = pst.executeQuery();
            while(rs.next()){
                barra_destino.addItem(rs.getObject(1).toString()+": "+rs.getObject(2).toString());
            }
            cn.close();
            
        } catch (Exception e) {
            System.out.println(e);
        }
    }
    
    

