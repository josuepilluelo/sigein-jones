        java.util.Date date = new java.util.Date();
        SimpleDateFormat f = new SimpleDateFormat("yyyy-MM-dd");
        Date fecha_1 = new Date();
        int validacion = 0;
        String numero_inventario, descripcion, marca, estado_actual, valor_estimado, tipo_sico_sareco, modelo, no_serie;
        String fecha = "";
        numero_inventario = etiqueta_no_inventario.getText().trim();
