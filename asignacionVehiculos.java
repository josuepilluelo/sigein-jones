    private void boton_asignarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_asignarActionPerformed
        //Se realiza la asignacion de bienes
        
//        System.out.println("Vehiculo "+id_vehiculo);
        asignacionVehiculosTabla abmt =  new asignacionVehiculosTabla();
        String empleado = barra_destino.getSelectedItem().toString();
        String datos_empleado[]=new String[2];
        StringTokenizer token = new StringTokenizer(empleado,":");
        while (token.hasMoreTokens()){
            datos_empleado[0] = token.nextToken();
        }                                                                                        
        System.out.println("Clave: "+datos_empleado[0]);
        System.out.println("Id del bien "+abmt.id_vehiculo);
        try {
            Connection cn = Conection_BD.conexion_bd();
            PreparedStatement pst = cn.prepareStatement("SELECT id_empleado FROM empleados WHERE clave_servidor_publico like '%"+datos_empleado[0].trim()+"%'");
            ResultSet rs = pst.executeQuery();
            while(rs.next()){
                String id_empleado=rs.getObject(1).toString();
                pst = cn.prepareStatement("UPDATE vehiculos SET id_empleado = ? WHERE id_vehiculo = "+abmt.id_vehiculo);
                pst.setString(1, id_empleado);
                pst.executeUpdate();
                System.out.println("Actualizacion exitosa!");
                
            }
            
            DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy-MM-dd");
            String fecha = dtf.format(LocalDateTime.now());
            pst = cn.prepareStatement("insert into movimientos (`tipo_movimiento`, `fecha`, `accion`, `tabla_afectada`) values (?,?,?,?)");
            pst.setString(1, "Asignacion de Vehiculo");
            pst.setString(2, fecha);
            pst.setString(3, "Update del Vehiculo con ID: "+abmt.id_vehiculo);
            pst.setString(4, "Vehiculos");
            pst.executeUpdate();
            cn.close();
            Icon icono_correcto = new ImageIcon(getClass().getResource("/imagenes/correcto.png"));
            int valor = JOptionPane.showConfirmDialog(null, "Se ha asignado correctamente", "Asignacion Correcta", JOptionPane.OK_OPTION, JOptionPane.QUESTION_MESSAGE, icono_correcto);
            abmt.setVisible(true);
            dispose();
            
        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(null, "Error al guardar ", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_boton_asignarActionPerformed

