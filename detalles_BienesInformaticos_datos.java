    public detalles_BienesInformaticos_datos() {
        initComponents();
        //cerrar();
        setIconImage(new ImageIcon(getClass().getResource("/imagenes/logoa.png")).getImage());
        id_bien = detalles_BienesInformaticos_tabla.id_bien;
        try {
            Connection cn = Conection_BD.conexion_bd();
            PreparedStatement pst = cn.prepareStatement("select "
                    + "b.id_bien, "
                    + "b.no_inventario, "
                    + "b.descripcion_bien, "
                    + "b.marca, "
                    + "b.estado_actual, "
                    + "b.valor_estimado, "
                    + "b.tipo_sico_sareco,"
                    + "b.modelo, "
                    + "b.no_serie, "
                    + "b.ultima_modificacion "
                    + "from bienes b "
                    + "where b.id_bien ='" + id_bien + "'");
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                id = rs.getInt("id_bien");
                etiqueta_no_inventario.setText(rs.getString("no_inventario"));
                etiqueta_descripcion.setText(rs.getString("descripcion_bien"));
                etiqueta_marca.setText(rs.getString("marca"));
                etiqueta_estado.setText(rs.getString("estado_actual"));//setSelectedItem(rs.getString("estado_actual"));
                etiqueta_valor.setText(rs.getString("valor_estimado"));
                etiqueta_tipo.setText(rs.getString("tipo_sico_sareco"));//.setSelectedItem(rs.getString("tipo_sico_sareco"));
                etiqueta_modelo.setText(rs.getString("modelo"));
                etiqueta_no_serie.setText(rs.getString("no_serie"));
                fecha.setText(rs.getString("ultima_modificacion"));
            }
            cn.close();
        } catch (Exception e) {
            System.out.println("Error al cargar usuario: " + e);
            //JOptionPane.showMessageDialog(Componente, mensaje, t√≠tulo, tipo de mensaje, icono);
            JOptionPane.showMessageDialog(null, "Error al al cargar informacion", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

