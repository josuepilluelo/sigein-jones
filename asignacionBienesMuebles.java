    public void cargarEmpleados() {
        try {
            Connection cn = Conection_BD.conexion_bd();
            PreparedStatement pst = cn.prepareStatement("SELECT nombre_empleado, clave_servidor_publico FROM empleados WHERE nombre_empleado not in ('almacen','transferencia')");
            ResultSet rs = pst.executeQuery();
            while (rs.next()) {
                barra_destino.addItem(rs.getObject(1).toString() + ": " + rs.getObject(2).toString());
            }
            cn.close();

        } catch (Exception e) {
            System.out.println(e);
        }
    }

    public void colocarValores() {
        asignacionBienesMueblesTabla abmt = new asignacionBienesMueblesTabla();
        String id = abmt.id;
        barra_noInventario.setText(id);

        try {
            Connection cn = Conection_BD.conexion_bd();
            PreparedStatement pst = cn.prepareStatement("select no_inventario, descripcion_bien, estado_actual, valor_estimado, tipo_sico_sareco, modelo, no_serie from bienes where id_bien = " + id);
            ResultSet rs = pst.executeQuery();
            while (rs.next()) {
                Object[] fila = new Object[5];//por las columnas que tengo
                barra_noInventario.setText(rs.getObject(1).toString());
                barra_descripcion.setText(rs.getObject(2).toString());
                barra_estado.setText(rs.getObject(3).toString());
                barra_valor.setText(rs.getObject(4).toString());
                barra_tipo.setText(rs.getObject(5).toString());
                barra_modelo.setText(rs.getObject(6).toString());
                barra_noSerie.setText(rs.getObject(7).toString());

                barra_noInventario.disable();
                barra_descripcion.disable();
                barra_estado.disable();
                barra_valor.disable();
                barra_tipo.disable();
                barra_modelo.disable();
                barra_noSerie.disable();
            }
            cn.close();
        } catch (Exception e) {
            System.out.println("La excepcion es: " + e);
        }
    }

